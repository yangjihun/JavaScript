// 동등연산자 ==
console.log(1==2);
console.log(1==1);
console.log("one"=="one");

// 일치연산자 ===
console.log(1=='1');
console.log(1==='1');

console.log(null == undefined);
console.log(null === undefined);
console.log(true==3);  // 1이 아니면 false
console.log(true===1);

// 부정
console.log(1!=2);
console.log(1!=1);
console.log(1!=='1');